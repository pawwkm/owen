bool semantics_expressions_address_of(void)
{
    static uint8_t state;
    switch (state++)
    {
        case 0:
            retain_parameter_reference();
            break;

        case 1:
            retain_parameter_array_access();
            break;

        case 2:
            retain_parameter_field_access();
            break;

        case 3:
            retained_parameter_as_actual_parameter_with_implicit_actual_type_parameters();
            break;

        case 4:
            non_retained_parameter_as_actual_parameter_with_implicit_actual_type_parameters();
            break;

        case 5:
            non_retained_parameter_as_actual_paramerter_with_explicit_type_parameters();
            break;

        case 6:
            retain_variable_reference();
            break;

        case 7:
            retain_variable_array_access();
            break;

        case 8:
            retain_variable_field_access();
            break;

        case 9:
            retained_variable_as_actual_parameter_with_implicit_actual_type_parameters();
            break;

        case 10:
            non_retained_variable_as_actual_parameter_with_implicit_actual_type_parameters();
            break;

        case 11:
            non_retained_variable_as_actual_paramerter_with_explicit_type_parameters();
            break;

        case 12:
            return_retained_parameter();
            break;

        case 13:
            return_non_retained_parameter();
            break;

        case 14:
            return_retained_variable();
            break;

        case 15:
            return_non_retained_variable();
            break;

        default:
            return false;
    }

    return true;
}
